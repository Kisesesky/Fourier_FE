export const shortcutToEmoji: Record<string, string> = Object.values(imogiShortcuts).reduce<Record<string, string>>(
  (acc, group) => {
    Object.entries(group).forEach(([emoji, shortcut]) => {
      acc[shortcut] = emoji;
    });
    return acc;
  },
  {},
);

export function replaceEmojiShortcuts(text: string): string {
  if (!text) return text;
  return text.replace(/\/[^\s]+/g, (token) => shortcutToEmoji[token] ?? token);
}
